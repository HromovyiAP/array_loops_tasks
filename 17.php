<h3>Составьте массив месяцев. С помощью цикла foreach выведите все месяцы, а текущий
    месяц выведите жирным. Текущий месяц должен храниться в переменной $month.</h3>

<?php
$month = "Лютий";
$arr = array ("Січень", "Лютий", "Березень",
    "Квітень", "Травень", "Червень", "Липень",
    "Серпень", "Вересень", "Жовтень", "Листопад", "Грудень");
foreach ($arr as $item) {
    if ($item === $month) { echo "<b> $item </b>";
    }
    else { echo $item;}
    }

?>